import type { Background, GradientStop, TemplateDef } from '../types';

// --- vibrant gradient helpers ------------------------------------------------
type Stop = GradientStop;
const lin = (angle: number, stops: Stop[]): Background => ({
  kind: 'linear',
  from: stops[0].color,
  to: stops[stops.length - 1].color,
  angle,
  stops,
});
const rad = (stops: Stop[]): Background => ({
  kind: 'radial',
  from: stops[0].color,
  to: stops[stops.length - 1].color,
  stops,
});

