/** Cell helper: position within panel `p` of an `n`-panel canvas. */
const inPanel = (
  p: number,
  n: number,
  x: number,
  y: number,
  w: number,
  h: number,
  r?: number,
) => ({
  x: (p + x) / n,
  y,
  w: w / n,
  h,
  ...(r !== undefined ? { r } : {}),
});

/** Cell helper: spans from panel-fraction start to end across an n-panel canvas. */
const span = (
  startPanelX: number,
  n: number,
  widthPanels: number,
  y: number,
  h: number,
  r?: number,
) => ({
  x: startPanelX / n,
  y,
  w: widthPanels / n,
  h,
  ...(r !== undefined ? { r } : {}),
});

